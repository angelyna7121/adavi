import { useState } from "react";
import { useQuery } from "@tanstack/react-query";
import { useParams, Link } from "wouter";
import { Printer, ArrowLeft, AlertTriangle, Zap, Lock } from "lucide-react";
import { Button } from "@/components/ui/button";
import { useAuth } from "@/hooks/use-auth";
import { NetWorthUpgradeModal } from "@/components/NetWorthUpgradeModal";
import type { Report, NetWorthStatement, NetWorthItem, IncomeStrategy } from "@shared/schema";

// ── types ─────────────────────────────────────────────────────────────────────

interface NetWorthDetail {
  statement: NetWorthStatement;
  items: NetWorthItem[];
}

interface ReportPayload {
  report: Report;
  detail: NetWorthDetail | IncomeStrategy | null;
  user: { email: string; profile: { firstName?: string; lastName?: string; province?: string } | null };
}

// ── helpers ───────────────────────────────────────────────────────────────────

const $ = (n: number) =>
  new Intl.NumberFormat("en-CA", { style: "currency", currency: "CAD", maximumFractionDigits: 0 }).format(n);

const fmtDate = (d: string | Date) =>
  new Date(d).toLocaleDateString("en-CA", { year: "numeric", month: "long", day: "numeric" });

const rptId = (id: number) => `RPT-${String(id).padStart(5, "0")}`;

const PROVINCES: Record<string, string> = {
  ON: "Ontario", BC: "British Columbia", AB: "Alberta", QC: "Quebec",
  SK: "Saskatchewan", MB: "Manitoba", NS: "Nova Scotia", NB: "New Brunswick",
  NL: "Newfoundland & Labrador", PE: "Prince Edward Island",
};

function isNetWorthDetail(d: any): d is NetWorthDetail {
  return d != null && "statement" in d && "items" in d;
}

const GOLD = "#C5A35A";
const NAVY = "#0D1929";

// ── print styles ──────────────────────────────────────────────────────────────

const PRINT_CSS = `
  @media print {
    .no-print { display: none !important; }
    body { background: white !important; -webkit-print-color-adjust: exact; print-color-adjust: exact; }
    .doc-page { box-shadow: none !important; border-radius: 0 !important; margin: 0 !important; }
  }
  @page { size: A4; margin: 1.5cm 2cm; }
`;

